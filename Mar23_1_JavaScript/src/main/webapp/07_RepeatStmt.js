// 7. 반복문
//	동일한 작업을 여러번 반복할 때 사용

// for / while / do-while

// 1 ~ 10까지 출력 (document.write())

for (let count = 1; count < 11; count++) {
	document.write(count + "<br>");
}
document.write("-----------------------------<br>");

let count1 = 1;
while(count1 <= 10) {
	document.write(count1 + "<br>");
	count1++;
}
document.write("-----------------------------<br>");

let count2 = 1;
do {
	document.write(count2 + "<br>");
	count2++;
} while (count2 < 11);
document.write("-----------------------------<br>");

